package com.app.vdlasov.tinkoffschool.ui.model;

public class DialogUiItem {

    private String title;
    private String desc;

    public DialogUiItem(String title, String desc) {
        this.title = title;
        this.desc = desc;
    }

    public String getTitle() {
        return title;
    }

    public String getDesc() {
        return desc;
    }
}
